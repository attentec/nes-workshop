.INCLUDE "nk.s"

.SEGMENT    "ZEROPAGE"

COUNTER:
    .RES 1

.SEGMENT    "CODE"

RESET:
    NK_CTRL_CONFIG  NK_CTRL_NMI
    NK_MASK_CONFIG  NK_MASK_TINT_BLUE
    LDX #%00000100
    STX $4015
    LDX #%10000001
    STX $4008
    STX $400A
    STX $400B
    LDX #0
    STX COUNTER
    RTS

UPDATE:
    RTS

VBLANK:
    DEC COUNTER
    LDA COUNTER
    STA $400A
    RTS
